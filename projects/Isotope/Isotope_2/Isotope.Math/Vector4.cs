namespace Isotope.Math
{
    public struct Vector4
    {
        public double M1;
        public double M2;
        public double M3;
        public double M4;

        public Vector4(double m1, double m2, double m3, double m4)
        {
            M1 = m1;
            M2 = m2;
            M3 = m3;
            M4 = m4;
        }
    }
}